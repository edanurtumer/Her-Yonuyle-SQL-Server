--DATEADD
DECLARE @TARIH1 AS DATETIME
SET @TARIH1='2019-01-01 16:21:37'
DECLARE @TARIH2 AS DATETIME
SET @TARIH2=DATEADD(MONTH,6,@TARIH1) --YEAR,DAY,HOUR,WEEK,MINUTE,SECOND EKLEMESÝ DE YAPILABÝLÝR
SELECT @TARIH1,@TARIH2

--DATEDIFF
DECLARE @TARIH1 AS DATETIME
	SET @TARIH1='2019-01-01 16:21:37'
DECLARE @TARIH2 AS DATETIME
	SET @TARIH2='2020-01-01 16:21:37'
SELECT DATEDIFF(MONTH,@TARIH1,@TARIH2)

--DATEFROMPARTS
DECLARE @TARIH AS DATETIME
SET @TARIH=DATEFROMPARTS(2020,8,21)
SELECT @TARIH

--DATEPART
DECLARE @TARIH AS DATETIME
SET @TARIH='2019-01-07 16:21:37'
SELECT DATEPART(DAY,@TARIH)

--GETDATE
SELECT GETDATE()